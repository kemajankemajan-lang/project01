const defaultReviews = [
  { name: 'Nimali', rating: 5, message: 'The kottu and hoppers were absolutely delicious.' },
  { name: 'Ravindu', rating: 4, message: 'Friendly service and wonderful Sri Lankan flavors.' }
];

function getStoredReviews() {
  try {
    const stored = localStorage.getItem('greenLeafReviews');
    return stored ? JSON.parse(stored) : defaultReviews;
  } catch (error) {
    return defaultReviews;
  }
}

function saveReviews(reviews) {
  try {
    localStorage.setItem('greenLeafReviews', JSON.stringify(reviews));
  } catch (error) {
    // Ignore storage errors and continue
  }
}

function renderReviews() {
  if (!reviewList) return;
  const reviews = getStoredReviews();

  if (!reviews.length) {
    reviewList.innerHTML = '<p class="review-item">No reviews yet. Be the first to leave one.</p>';
    return;
  }

  reviewList.innerHTML = reviews.map((review) => `
    <div class="review-item">
      <strong>${review.name}</strong>
      <div class="review-stars">${'★'.repeat(review.rating)}${'☆'.repeat(5 - review.rating)}</div>
      <p>${review.message}</p>
    </div>
  `).join('');
}

function getStoredLanguage() {
  try {
    return localStorage.getItem('greenLeafLang') || 'en';
  } catch (error) {
    return 'en';
  }
}

function saveLanguage(lang) {
  try {
    localStorage.setItem('greenLeafLang', lang);
  } catch (error) {
    // Ignore storage errors and continue with in-memory state
  }
}

function setLanguage(lang) {
  currentLang = translations[lang] ? lang : 'en';
  const selected = translations[currentLang];

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (selected[key]) {
      el.textContent = selected[key];
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (selected[key]) {
      el.placeholder = selected[key];
    }
  });

  document.documentElement.lang = currentLang;

  langButtons.forEach((btn) => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
  });

  if (reservationMessage) {
    reservationMessage.textContent = currentLang === 'en'
      ? 'Reservation request received! Our team will contact you shortly.'
      : currentLang === 'ta'
        ? 'முன்பதிவு கோரிக்கை பெறப்பட்டது! எங்கள் குழு விரைவில் உங்களைத் தொடர்புகொள்ளும்.'
        : 'ඉල්ලීම ලැබී ඇත! අපගේ කණ්ඩායම ඉක්මනින් ඔබව සම්බන්ධ කර ගනු ඇත.';
  }
}

